package com.sa.data;

public class screen {
int id;
int rows;
int cols;
String screen;
public String getScreen() {
	return screen;
}
public void setScreen(String screen) {
	this.screen = screen;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getRows() {
	return rows;
}
public void setRows(int rows) {
	this.rows = rows;
}
public int getCols() {
	return cols;
}
public void setCols(int cols) {
	this.cols = cols;
}
}
